def generate_server(
    client: LLMClient,
    scene_data: dict,
    output_path: Path,
    *,
    predefined_skills: list[dict] | None = None,
) -> list[str]:
    """Generate server.py and return the list of all tool names.

    Tool generation priority:
    1. BASE_SERVER_TEMPLATE platform tools (always included)
    2. Predefined skills from YAML (auto-generated, no LLM needed)
    3. LLM-generated tools (only for ``extra_tools`` not covered above)
    """
    scene_display_name = scene_data["scene_display_name"]
    extra_tools_from_analysis = scene_data.get("extra_tools", [])

    code_parts: list[str] = []
    predefined_tool_names: list[str] = []

    # --- Priority 2: predefined skills from YAML ---
    if predefined_skills:
        skills_needing_code = filter_skills_needing_code(predefined_skills)
        if skills_needing_code:
            predefined_code = generate_all_tool_code(skills_needing_code)
            code_parts.append(
                "# ---------------------------------------------------------------------------\n"
                "# Predefined scene skills (from refinev1_updated_with_io.yaml)\n"
                "# ---------------------------------------------------------------------------\n\n"
                + predefined_code
            )
            predefined_tool_names = get_predefined_tool_names(predefined_skills)
            logger.info(
                "Generated %d predefined scene tools: %s",
                len(predefined_tool_names),
                ", ".join(predefined_tool_names[:10])
                + ("..." if len(predefined_tool_names) > 10 else ""),
            )

    # --- Priority 3: LLM-generated tools (only for uncovered extra_tools) ---
    uncovered = [
        t for t in extra_tools_from_analysis
        if t not in predefined_tool_names and t not in _BASE_TOOL_NAMES_SET
    ]
    llm_tool_names: list[str] = []
    if uncovered:
        llm_code = _generate_llm_tools(
            client, scene_data, uncovered, predefined_tool_names,
        )
        if llm_code:
            code_parts.append(
                "# ---------------------------------------------------------------------------\n"
                "# LLM-generated tools (not covered by predefined skills)\n"
                "# ---------------------------------------------------------------------------\n\n"
                + llm_code
            )
            llm_tool_names = uncovered
            logger.info("LLM generated %d extra tools: %s", len(uncovered), uncovered)

    extra_code = "\n\n".join(code_parts)

    server_code = BASE_SERVER_TEMPLATE.format(
        scene_display_name=scene_display_name,
        extra_tools_code=extra_code,
    )

    output_path.write_text(server_code, encoding="utf-8")
    logger.info("Wrote %s", output_path)

    all_tools = list(_BASE_TOOL_NAMES_SET) + predefined_tool_names + llm_tool_names
    # Preserve a stable ordering: base tools first in their canonical order.
    from ..config import BASE_TOOLS
    all_tools = list(BASE_TOOLS) + predefined_tool_names + llm_tool_names

    return all_tools


_BASE_TOOL_NAMES_SET = {
    "move", "move_to", "grasp_start", "place_start", "place",
    "reach_out", "release_hand", "adjust_body_height", "handover",
    "spatial_memory_query_vec", "delete_spatial_memory_by_id",
    "scene_recognition", "trigger_spatial_processing",
    "get_robot_pose", "place_down", "back_station",
    "parameter_store", "perception_custom", "fetch_box", "put_box",
}


def _generate_llm_tools(
    client: LLMClient,
    scene_data: dict,
    unknown_tools: list[str],
    predefined_tool_names: list[str],
) -> str:
    """Call the LLM to generate MCP tool code for truly unknown capabilities."""
    furniture_list = ", ".join(f["name"] for f in scene_data.get("furniture", []))
    objects_list = ", ".join(o["name"] for o in scene_data.get("objects", []))

    all_existing = list(_BASE_TOOL_NAMES_SET) + predefined_tool_names
    prompt = EXTRA_TOOLS_PROMPT.format(
        scene_display_name=scene_data["scene_display_name"],
        extra_tools=", ".join(unknown_tools),
        furniture_list=furniture_list,
        objects_list=objects_list,
        existing_tools=", ".join(all_existing),
    )
    messages = [{"role": "user", "content": prompt}]
    result = client.chat_json(messages)

    parts = []
    for tool_def in result.get("tools", []):
        sanitized = _sanitize_tool_code(tool_def["code"])
        parts.append("\n" + sanitized + "\n")
    return "\n".join(parts)


def _sanitize_tool_code(code: str) -> str:
    """Normalize LLM-generated tool code to avoid simple signature syntax bugs."""
    if not code:
        return code
    return _DEF_BLOCK_RE.sub(_rewrite_function_signature, code)


def _rewrite_function_signature(match: re.Match[str]) -> str:
    indent = match.group("indent")
    name = match.group("name")
    params_block = match.group("params")
    param_indent = indent + "    "

    params = _parse_param_lines(params_block)
    required = [p for p in params if not p["has_default"]]
    optional = [p for p in params if p["has_default"]]
    ordered = required + optional

    rendered_params = "".join(
        f"{param_indent}{param['text']},\n"
        for param in ordered
    )
    return f"{indent}def {name}(\n{rendered_params}{indent}) -> dict:\n"


def _parse_param_lines(params_block: str) -> list[dict[str, object]]:
    params: list[dict[str, object]] = []
    for raw_line in params_block.splitlines():
        line = raw_line.strip()
        if not line:
            continue
        if line.endswith(","):
            line = line[:-1].rstrip()
        if not line:
            continue
        params.append({
            "text": line,
            "has_default": _param_has_default(line),
        })
    return params


def _param_has_default(param_text: str) -> bool:
    depth = 0
    for ch in param_text:
        if ch in "([{":
            depth += 1
        elif ch in ")]}":
            depth = max(0, depth - 1)
        elif ch == "=" and depth == 0:
            return True
    return False
